	 		<h4>OUR HISTORY</h4><br>
	 		<p>
<li><b>Throwback</b><br>
Going back in time to pre 1980s when Titan Watches was an idea conceived at the Tata Press. Turning this idea of venturing into the Watch Industry into a winning reality, Titan Watches was a result of a joint venture with the Tata Industries with the Tamilnadu Industrial Development Corporation (TIDCO) in June of 1984. Then on, time never stopped ticking for Titan.</li><br>

<li><b>1980s
First Milestone</b><br>
Production commenced in 1986 and March, 1987 saw the launch of the brand Titan. It sprung to instant claim with the analogue electronic Quartz watches bringing in a paradigm shift to the Indian watch market. This paved way for further extension to bracelets, cases, electronic circuitry and step motors.</li><br>

<li><b>1990s
The Journey</b><br>
A journey of over 25 years that saw Titan Watch Project becoming Titan Watches, Titan Industries and to what’s today known as the Titan Company Limited. With hitting the 15 million mark every year in production, each year saw new technologies and innovations. Entered European market in 1993, followed by Middle East and Asia Pacific,Titan watches sold over 150 million pieces across 32 countries. Topping the charts in the share of the organized watch market with a claim of 50% share, audaciously challenging the Swiss Titan became the world's largest integrated own brand manufacturer of watches.<br>

The main manufacturing unit is set up at Hosur, Tamilnadu along with assembly units in Dehradun, Pantnagar and Roorkee and an electronic sub assembly plant in Goa in India.<br>

Timex was a result of a brief joint venture between the year 1992 and 1998 claiming 2nd position next to Titan in popularity. Titan after dissolution of this partnership, reclaimed the share, with the launch of Sonata. This was followed by an astute array of wrist wear - Titan, Fastrack, and Xylys brands, luxury watches and designer timescapes - a range of outdoor clocks that defined spectacular landmarks in several cities.<br>

With the state of art manufacturing unit, strong and consistent market share and the highly trained and dedicated workforce, left the Swiss and Japanese manufactures astounded. Further accentuating the Tata spirit was the township that was built by the world renowned architect, Charles Correa. Accommodating over 2000 Titanians an excellent school that doubles as a learning facility for neighboring district schools was built in Mathigiri, Tamilnadu.</li><br>


<li><b>2000s
The Expansion</b><br>
Along with conquering the watch market, Titan made it’s entry to the glittering side. Expanding to jewellery in 1993, the brand Tanishq was launched in 1996 and a decade later launched Goldplus - the standardized mass marketing of jewellery. This diversification was further followed by more ventures. Fastrack – the fun and youthful brand of watches and accessories, and accessories line in 2003; Precision Engineering in 2005, and the Titan Eyewear Division in 2007 - a project of prescription eyewear, complete with specialized outlets, catering to customers’ every need.<br>

Focusing exclusively on the home market that views gold as a prime investment, with Tanishq and Goldplus Titan Company hit the right chord with their offering of traditional and designer jewellery. Tanishq offers a stunning world of exquisite, precious designer ware through over 160 stores in 89 cities across the nation. Goldplus has a proud market share of around 40% in the semi-urban and rural scenario. These two ventures are a pride to India and to Titan Company, for they certainly keep value conscious customers happy and contented.</li><br>

<li><b>2010s
The New Editions</b><br>
In 2011, Titan launched Mia, a stunning range of fine, urban chic, affordable jewellery crafted for working women. All the pieces in Mia are crafted in 14K gold and has over 500 designs.</li><br>

<li><b>2013</b><br>
 saw Titan penetrating the perfume market by launching SKINN, redefining the Indian fragrance industry by assuring the highest quality and bearing the hallmark of Titan.</li><br>

<li><b>In 2016</b><br> Tanishq took over Carat Lane, to offer beautiful jewellery and a distinctive shopping experience that fits today’s values and lifestyles.</li><br>

<li>Exactly five years since it acquired the Swiss watch brand Favre-Leuba in 2011, Titan relaunched the iconic watch brand in 2016, which was a household name in India in the 1960s and 1970s.</li>
